#!/usr/bin/env python3
"""Concatenation with Annotation"""


def concat(str1: str, str2: str) -> str:
    """
    Concatenates strings as one

    Arguments:
      str1: type string
      str2: type string

    Returns:
      concat_string: type string
    """
    concat_string: str = str1 + str2
    return concat_string
